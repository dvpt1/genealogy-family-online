<p>
7.1.
Responsabile è l'amministrazione del sito
per la divulgazione intenzionale di dati personali
Dati utente in conformità con
normativa vigente, per
salvo casi previsti
p.p. 5.2., 5.3. e 7.2. di questa Politica
Privacy.</p>
<p>
7.2.
In caso di smarrimento o divulgazione
Amministrazione dei dati personali
il sito non è responsabile se
queste informazioni riservate:</p>
<p>
7.2.1.
Divenne di pubblico dominio fino alla sua perdita
o divulgazione.</p>
<p>
7.2.2.
È stato ricevuto in precedenza da una terza parte
al momento della sua ricezione da parte dell'Amministrazione
sito.</p>
<p>
7.2.3.
È stato ottenuto da terzi attraverso
accesso non autorizzato ai file
sito.</p>
<p>
7.2.4.
È stato divulgato con il consenso dell'utente.</p>
<p>
7.3.
L'utente è responsabile
legalità, correttezza e veridicità
dati personali forniti
in conformità con la corrente
legislazione.</p>
