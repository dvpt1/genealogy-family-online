<p>
6.1.
L'utente si impegna:</p>
<p>
6.1.1.
Fornire informazioni corrette e veritiere
informazioni sui dati personali,
necessario per utilizzare il sito.</p>
<p>
6.1.2.
Aggiornare o integrare quanto fornito
informazioni sui dati personali in
se queste informazioni cambiano.</p>
<p>
6.1.3.
Adottare misure per proteggere l'accesso a
i tuoi dati riservati archiviati
sul sito web.</p>
<p>
6.2.
L'amministrazione del sito si impegna a:</p>
<p>
6.2.1.
Utilizzare le informazioni ricevute
esclusivamente per gli scopi specificati in
clausola 4 della presente Informativa sulla privacy.</p>
<p>
6.2.2.
Non divulgare dati personali
Utente, ad eccezione dei paragrafi. 5.2. E
5.3. della presente Informativa sulla privacy.</p>
<p>
6.2.3.
Blocca personale
dati relativi al relativo
L'utente, dal momento del contatto o
richiesta dell'Utente o del suo legale
rappresentante o autorizzato
organismo per la tutela dei diritti dei soggetti
dati personali per il periodo di verifica,
in caso di rilevamento di inaffidabili
dati personali o illegali
azioni.</p>
