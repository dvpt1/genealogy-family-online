<h2>5. MODALITÀ E TERMINI DEL TRATTAMENTO DEI DATI PERSONALI</h2>
<p>
5.1.
Trattamento dei dati personali dell'Utente
effettuato senza limiti di tempo,
con qualsiasi mezzo legale, incluso
sistemi di informazione personale
dati utilizzando strumenti
automazione o senza
tali fondi.</p>
<p>
5.2.
L'utente è d'accordo
L'amministrazione del sito ha il diritto di trasferimento
dati personali a terzi, in
in particolare, servizi di corriere,
organizzazioni postali, operatori
telecomunicazioni, esclusivamente per gli scopi
evasione delle richieste dell'Utente,
pubblicato sul sito web, nell'ambito dell'Accordo
offerta pubblica.</p>
<p>
5.3.
I dati personali dell'Utente possono
essere trasferiti agli enti autorizzati
solo potere statale
per le ragioni e secondo le modalità stabilite
legislazione attuale.</p>
