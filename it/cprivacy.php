derivanti dal rapporto tra
Utente e amministrazione del sito
sito web, è richiesta la presentazione
rivendicazioni (proposta scritta per
risoluzione volontaria della controversia).</p>
<p>
8.2
.Il destinatario del reclamo entro 30
giorni di calendario dalla data di ricevimento
reclami, ne dà comunicazione scritta al richiedente
