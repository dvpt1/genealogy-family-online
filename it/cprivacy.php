<h1>Informativa sulla privacy</h1>
<p>01 gennaio 2024 — Aggiornato: 24 marzo 2024</p>
<br>
<p>
Vero
Informativa sulla privacy personale
dati (di seguito denominata Privacy Policy)
si applica a tutte le informazioni
quale questo sito su cui si trova
il testo della presente Informativa sulla privacy,
può ottenere informazioni sull'Utente, nonché
eventuali programmi e prodotti ospitati
su di esso.</p>
<h2>1. DEFINIZIONE DEI TERMINI</h2>
<p>
1.1
Nella presente Informativa sulla privacy
Vengono utilizzati i seguenti termini:</p>
<p>
1.1.1.
“Amministrazione del sito” – autorizzato
personale di gestione del sito,
agendo per suo conto, chi
organizzare e (o) effettuare il trattamento
dati personali e determina anche
finalità del trattamento dei dati personali,
composizione dei dati personali oggetto dell'art
elaborazione, azioni (operazioni) eseguite
con dati personali.</p>
<p>
1.1.2.
“Dati personali” - qualsiasi informazione
relativi direttamente o indirettamente
definito o determinabile
a un individuo (oggetto di personali
dati).</p>
<p>
1.1.3.
“Trattamento dati personali” - eventuale
azione (operazione) o insieme
azioni (operazioni) eseguite con
utilizzando strumenti di automazione
o senza l'uso di tali mezzi con
dati personali, compresa la raccolta,
registrazione, sistematizzazione, accumulazione,
archiviazione, chiarimento (aggiornamento,
modifica), estrazione, utilizzo,
trasmissione (distribuzione, fornitura,
accesso), depersonalizzazione, blocco,
rimozione, distruzione di dati personali
dati.</p>
<p>
1.1.4.
"Riservatezza personale
dati" - adempimento obbligatorio
L'amministrazione del sito non richiede
consentirne la deliberata distribuzione
senza il consenso dell'interessato
dati o la presenza di altri legali
motivi.</p>
<p>
1.1.5.
“Utente del sito (di seguito denominato Utente)”
– soggetto che ha accesso al sito tramite
Internet e l'utilizzo di questo
sito web per i tuoi scopi.</p>
<p>
1.1.6.
I "cookie" sono una piccola porzione di dati
inviati dal server web e memorizzati
sul computer dell'utente
client Web o browser Web ogni volta
invia al server web una richiesta HTTP quando
cercando di aprire la pagina corrispondente
sito.</p>
<p>
1.1.7.
L'“indirizzo IP” è un indirizzo di rete univoco
nodo in una rete di computer costruita
tramite protocollo IP.</p>
<h2>2. DISPOSIZIONI GENERALI</h2>
<p>
2.1.
Utilizzo del sito da parte dell'utente
significa accordo con questa Politica
privacy e condizioni di trattamento
dati personali dell'Utente.</p>
<p>
2.2.
In caso di disaccordo con i termini della Politica
privacy L'utente deve
smetti di usare il sito.</p>
<p>
2.3.Reale
Si applica l'informativa sulla privacy
solo a questo sito. Amministrazione
non controlla il sito e non ne è responsabile
responsabilità per siti di terzi,
a cui l'Utente può recarsi
tramite i link disponibili su questo sito.</p>
<p>
2.4.
L'amministrazione del sito non controlla
affidabilità dei dati personali,
fornito dall'utente del sito.</p>
<h2>3. OGGETTO DELLA PRIVACY POLICY</h2>
<p>
3.1.
Questa Informativa sulla Privacy
stabilisce gli obblighi dell'Amministrazione
sito sulla non divulgazione intenzionale
dati personali che l'Utente
prevede diverse richieste
Amministrazione del sito (ad esempio, quando
registrarsi sul sito, effettuare un ordine,
abbonamenti alle notifiche, ecc.)</p>
<p>
3.2.
Dati personali consentiti
trattamento ai sensi della presente Politica
riservatezza fornita
Dall'utente mediante compilazione
moduli speciali sul Sito e di solito
includere le seguenti informazioni:</p>
<p>
3.2.1.
cognome, nome, patronimico dell'Utente;</p>
<p>
3.2.2.
Numero di telefono di contatto dell'utente;</p>
<p>
3.2.3.
indirizzo email (e-mail);</p>
<p>
3.2.4.
luogo di residenza dell'Utente e di altri
dati.</p>
<p>
3.3.
Anche l'amministrazione del sito accetta
sforzi per proteggere i dati personali,
a cui vengono automaticamente trasferiti
processo di visita delle pagine del sito:</p>
<p>
IP
indirizzo;<br>
informazioni dai cookie;<br>
informazione
sul browser (o altro programma che
accede al sito);<br>
tempo
accesso;<br>
indirizzi visitati
pagine;<br>
referrer (indirizzo del precedente
pagine), ecc.</p>
<p>
3.3.1.
La disabilitazione dei cookie potrebbe rendere impossibile farlo
accesso al sito.</p>
<p>
3.3.2.
Il sito raccoglie statistiche riguardo
Indirizzi IP dei tuoi visitatori. Questo
le informazioni vengono utilizzate per identificare
e risolvere problemi tecnici, per
controllo della correttezza dell'effettuazione
operazioni.</p>
<p>
3.4.
Qualsiasi altra informazione personale
non specificato sopra (cronologia degli acquisti,
browser e sistemi operativi utilizzati
sistemi, ecc.) non è soggetto ad intenzionalità
divulgazione, salvo casi
previsto nei paragrafi. 5.2. e 5.3. Questo
Informativa sulla privacy</p>
